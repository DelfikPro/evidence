package evidence.render;

public class Vi {

	public int x, y, z;
	public double u, v;

	public Vi() {
	}

	public Vi(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	public Vi(int x, int y, int z, double u, double v) {
		this.x = x;
		this.y = y;
		this.z = z;
		this.u = u;
		this.v = v;
	}

}
